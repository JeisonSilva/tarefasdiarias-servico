package org.jsonapp.gestaoadministrativa;

public class AlunoDto {
    String email;
    String nome;

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }
}
